function white(text)
  return '\\cs(255,255,255)'..text
end

function green(text)
  return '\\cs(0,255,0)'..text
end

function yellow(text)
  return '\\cs(255,255,0)'..text
end

function red(text)
  return '\\cs(255,128,128)'..text
end

function darkRed(text)
  return '\\cs(255,0,0)'..text
end

function ice(text)
  return '\\cs(150,200,255)'..text
end
